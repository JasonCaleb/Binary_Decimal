# Hexadecimal to Decimal

# just in case if the user inputted is not a number
hexadecimal = (str(input("Input the hexadecimal number: "))).upper()
is_hexadecimal = True
all_hexadecimal_numbers = "0123456789ABCDEF"

# to check if the entered number is a hexadecimal number
for i in str(hexadecimal):
  if i not in all_hexadecimal_numbers:
    is_hexadecimal = False
    break
    
# to process the conversion of hexadecimal number to decimal number
if is_hexadecimal == True:


  # translating the hexadecimal to decimal
  def get_the_index(hexadecimal_element):
    index = 0
    for i in all_hexadecimal_numbers:
      if i == hexadecimal_element:
        break

      else:
        index += 1

    return index


  pre_hexadecimal = []
  for i in hexadecimal:
    pre_hexadecimal.append(str(get_the_index(i)))

  # converting needs to read the hexadecimal numbers from back to front
  standard_number = len(pre_hexadecimal) - 1
  decimal = 0

  for i in pre_hexadecimal:
    decimal += int(i)*(16**standard_number)
    standard_number -= 1

  print(decimal)

# a warn message for the user that the entered number is not a hexadecimal number
else:
  print("the inputted number is not a hexadecimal number")