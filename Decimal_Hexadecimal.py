# Decimal to Hexadecimal

# just in case if the user inputted is not a number
try:
  decimal = int(input("Input the decimal number: "))
  hexadecimal = ""
  all_hexadecimal_numbers = "0123456789ABCDEFG"

  # deviding the inputted number with 2 until it remains 0 and
  # store the remainder of the division in a variabel named "hexadecimal"
  while decimal is not 0:
    
    # translating the decimal to hexadecimal
    pre_hexadecimal = str(decimal%16)
    hexadecimal += all_hexadecimal_numbers[int(pre_hexadecimal)]
    decimal //= 16

  # reversing the number in variabel named "hexadecimal"
  final_hexadecimal = ""
  standard_number = -1

  for i in range(len(hexadecimal)):
    if i == 0:  # the condition when accessing the first number from the back
      operation_number = i + standard_number
      final_hexadecimal += hexadecimal[operation_number]
     
    else: # the rest
      operation_number = (i * standard_number)+(-1)
      final_hexadecimal += hexadecimal[operation_number]

  print(final_hexadecimal)

# a warn message for the user that the entered number is not a number
except ValueError:
  print("the inputted number is not a number")



